            <h1>Tax Lawyer in Pakistan - File your Income tax Return
            </h1>
            <p>
                Do you have to pay more taxes on being non-taxpayer? Do you want to reduce your tax bill by becoming a
                taxpayer or do you need to claim a tax refund? Are you so busy that you don't have time to think about
                responding to tax notices?

            <p>If you don't file a tax return, you may have to pay an additional tax when you register, buy, or transfer
                a car. You will also have to pay double tax on the purchase and sale of the property.</p>
            <p>In addition, non-consolidated entities are subject to double taxation on their business activities (e.g.,
                providing services, selling goods, entering into contracts).</p>


            <p>To avoid being taxed twice, you must become a taxpayer.</p>
            <p>A taxpayer is a person whose name appears on a valid list of taxpayers by filing an annual income tax
                return and a wealth tax return.</p>

            <h4>Steps to follow</h4>
            <h4> Register for IRIS</h4>
            <p>Register for IRIS, the online tax filing portal, and file your tax return online.
                If you are a first-time tax filer, you will need to register before you can file your tax return.
                Once you have registered, you will be able to access Iris to complete your tax return. If you have
                received a National Tax Number (NTN) or Registration Number and do not have the login information to
                register with Iris, click on "E-registration for registrants" to access it.</p>

            <h4> IRIS access password</h4>
            <p> When you register electronically with the Federal Board of Revenue (FBR), you will be assigned a
                registration number or NTN and a password. Enter your Registration Number or NTN and password to access
                Iris.</p>

            <h4> Reset your password</h4>
            If you need to reset your password to access Iris, you can do so by clicking on "Forgot Password" on the
            Iris login screen. Please fill in all required fields.
            A code will be sent to your email address and cell phone number. Enter the code in the appropriate field. A
            new window will open allowing you to reset your password.

            <h4> Change your IRIS password</h4>
            <p>You can change your password by logging into Iris and clicking on "Change Password" in the upper right
                corner of the screen. Enter the required information in the dialog box that opens.</p>

            <h4> Preparing your tax return</h4>
            <p> To complete your tax return online, you will need to prepare a tax return and a wealth statement
                (statement of assets and liabilities).
                To make it easier for taxpayers, the FBR Knowledge Base portal provides step-by-step instructions and
                video tutorials on income and wealth tax returns.
                Once the income tax return and wealth tax return have been moved from the Iris Drafts folder to the
                Completed Tasks folder, both forms have been successfully accounted for.</p>

            <h4> Reconciliation of Wealth Tax Returns</h4>
            <p> If your income is greater than/less than your expenses and your assets have increased/decreased by the
                same amount this year as last year, you can successfully file an estate tax return. If you do not file
                an estate tax return, you will not be able to file a tax return.</p>

            <h4> Employees: Income tax return</h4>
            <p>To facilitate the reporting of employees' income, the 114(I) Declaration Form has been introduced.
                Employees must complete the 114(I) Declaration Form to file their tax return.
                This form can be used by individuals who only receive allowances or other sources that exceed 50% of
                their income.</p>

            <h4> Changing the IRIS PIN</h4>
            <p>To change the PIN, go to "Iris" and click on "Change PIN" at the top of the screen. Enter the required
                information in the dialog box that opens.</p>

            <h4> Completing the tax return</h4>
            <P>Tax returns can be amended to correct oversights or errors that are subsequently discovered within five
                years of the original filing.
                To amend a tax return that has been filed, an amended return must be submitted to Iris. Once approved,
                you may file an amended return with Iris.</P>

